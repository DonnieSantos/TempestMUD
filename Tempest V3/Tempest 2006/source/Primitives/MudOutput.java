import java.util.ArrayList;

class MudOutput

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private ArrayList output;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public MudOutput()           { output = new ArrayList();        }
  public void add(MudString s) { output.add(s);                   }
  public void add(String s)    { output.add(new MudString(s));    }
  public String toString()     { return toMudString().toString(); }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public MudString toMudString()

  {
    MudString allOutput = new MudString();

    for (int i=0; i<output.size(); i++)

    {
      MudString s = (MudString) output.get(i);
      allOutput.append(s);
    }

    return allOutput;
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}