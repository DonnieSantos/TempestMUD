import java.util.Iterator;

class DataCollection extends Data

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private HashList collection;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public String value()             { return "";                      }
  public void setValue(String s)    { return;                         }
  public Iterator <Data> iterator() { return collection.iterator();   }
  public Data get(String objName)   { return collection.get(objName); }
  public int size()                 { return collection.size();       }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public DataCollection(String collectionName)

  {
    dataID = COLLECTION;
    name = collectionName;
    collection = new HashList(51);
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public void add(Data data)

  {
    super.add(data);
    collection.add(data.name(), data);
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public void remove(Data data)

  {
    super.remove(data);
    collection.remove(data.name());
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}