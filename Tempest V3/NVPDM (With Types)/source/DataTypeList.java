import java.io.*;
import java.util.*;

class DataTypeList

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private ArrayList dataTypeList;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public int size()                  { return dataTypeList.size();            }
  public DataType getDataType(int i) { return (DataType) dataTypeList.get(i); }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public DataTypeList(ArrayList list)

  {
    dataTypeList = list;
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public DataType getDataType(String str)

  {
    // Replace this with Hash Table Lookup later...

    for (int i=0; i<dataTypeList.size(); i++)
      if (str.equalsIgnoreCase(getDataType(i).name()))
        return getDataType(i);

    return null;
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}