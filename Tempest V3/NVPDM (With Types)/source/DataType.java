import java.util.*;

class DataType

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private ArrayList vars;
  private ArrayList types;
  private String dataTypeName;
  private boolean collection;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public int size()             { return vars.size();           }
  public String name()          { return dataTypeName;          }
  public String getVar(int i)   { return (String) vars.get(i);  }
  public String getType(int i)  { return (String) types.get(i); }
  public boolean isObject()     { return (!collection);         }
  public boolean isCollection() { return (collection);          }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public DataType(String name, boolean isCollect)

  {
    dataTypeName = name;
    collection = isCollect;
    vars = new ArrayList();
    types = new ArrayList();
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public void addVariable(String varName)

  {
    if (!isObject()) return;

    for (int i=0; i<vars.size(); i++)
      if (getVar(i).equalsIgnoreCase(varName))
        return;

    vars.add(varName);
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public void addCollectionItem(String varName, String typeName)

  {
    if (!isCollection()) return;

    for (int i=0; i<vars.size(); i++)
      if (getVar(i).equalsIgnoreCase(varName))
        return;

    vars.add(varName);
    types.add(typeName);
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}