import java.util.ArrayList;
import java.util.Hashtable;
import java.util.Enumeration;

class DataObject extends Data

{
  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  private Hashtable vars;

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public String value()             { return "";                       }
  public void setValue(String val)  { return;                          }
  public void add(Data var)         { vars.put(var.name(), var);       }
  public int size()                 { return vars.size();              }
  public Data get(int i)            { return null;                     }
  public Data get(String objName)   { return (Data) vars.get(objName); }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public DataObject(String objectName)

  {
    dataID = OBJECT;
    name = objectName;
    vars = new Hashtable();
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////

  public ArrayList toList()

  {
    ArrayList A = new ArrayList();
    Enumeration enumeration = vars.elements();
    for (int i=0; i<vars.size(); i++) A.add(enumeration.nextElement());
    return A;
  }

  /////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////
}