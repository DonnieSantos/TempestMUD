import java.util.*;
import java.io.*;

public class Alias implements Externalizable

{
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 6;

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  String name;
  String output;
  
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////
  
  public void setName(String s)   { name = s;   }
  public void setOutput(String s) { output = s; }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public Alias()

  {
    name = "";
    output = "";
  }
  
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////
  
  public Alias replicate()
  
  {
    Alias A = new Alias();
    A.setName(new String(name));
    A.setOutput(new String(output));
    return A;
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public void writeExternal(ObjectOutput out)

  {
    try

    {
      out.writeObject("NAME");   out.writeObject(name);
      out.writeObject("OUTPUT"); out.writeObject(output);
      out.writeObject("ALIAS END");
    }

    catch (Exception e) { System.out.println(e); }
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public void readExternal(ObjectInput in)

  {
    try

    {
      String tag = "";

      while (!tag.equals("ALIAS END"))

      {
        tag = (String)in.readObject();

        if (tag.equals("NAME"))            name = (String)in.readObject();
        else if (tag.equals("OUTPUT"))     output = (String)in.readObject();
        else if (!tag.equals("ALIAS END")) in.readObject();
      }
    }

    catch (Exception e) { System.out.println(e); }
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////
}