import java.io.*;
import java.util.*;

public class EffectShield extends Effect

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 3000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void tick() { }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public EffectShield()

  {
    super();

    id = EFFECT_SHIELD;
    name = "Shield";
    desc = "Bolsters your defense against physical damage.";
    dmsg = "You feel less protected.";
    wmsg = "You are protected by Shield.";
    omsg = " is protected by Shield.";
    sign = "#N[#CS#N]";
    harmful = false;
    active = false;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void init(Object Owner, int Strength, int Duration)

  {
    initialize(Owner, Strength, Duration);
    owner = (Entity) Owner;

    if (getEntityOwner().addEffect(this))
      getEntityOwner().setAC(getEntityOwner().getAC()+30);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void dissipate(boolean audible)

  {
    getEntityOwner().removeEffect(this);
    getEntityOwner().setAC(getEntityOwner().getAC()-30);
    if (audible) getEntityOwner().echo(dmsg);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}