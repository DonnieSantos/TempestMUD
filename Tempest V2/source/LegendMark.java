import java.io.*;
import java.util.*;

class LegendMark implements Externalizable, Cloneable

{
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 15;

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  String mark;
  String date;
  int times;

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public LegendMark() { }
 
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////
  
  public LegendMark replicate()
  
  {
    LegendMark LM = null;
    try { LM = (LegendMark)clone(); }
    catch (Exception e) { System.out.println(e); }
    return LM;
  }
  
  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public LegendMark(String Mark, String Date, int Times)

  {
    mark = Mark;
    date = Date;
    times = Times;
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public void writeExternal(ObjectOutput out)

  {
    try

    {
      out.writeObject("MARK");   out.writeObject(mark);
      out.writeObject("DATE");   out.writeObject(date);
      out.writeObject("TIMES");  out.writeObject(new Integer(times));
      out.writeObject("LEGENDMARK END");
    }

    catch (Exception e) { e.printStackTrace(); }
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////

  public void readExternal(ObjectInput in)

  {
    try

    {
      String tag = "";

      while (!tag.equals("LEGENDMARK END"))

      {
        tag = (String)in.readObject();

        if (tag.equals("MARK"))       mark  = (String)in.readObject();
        else if (tag.equals("DATE"))  date  = (String)in.readObject();
        else if (tag.equals("TIMES")) times = ((Integer)in.readObject()).intValue();
        else if (!tag.equals("LEGENDMARK END")) in.readObject();
      }

    }

    catch (Exception e) { e.printStackTrace(); }
  }

  //////////////////////////////////////////////////////////////////////////////////
  //////////////////////////////////////////////////////////////////////////////////
}