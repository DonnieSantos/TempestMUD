class Flash extends SpellAreaDamage

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Flash()

  {
    super();

    id = SPELL_FLASH;
    name = "Flash";
    desc = "Blinding light damages all nearby enemies.";
    prof = "Mg Wi Il Sc Dm Wl Sh";
    manacost = 30;
    reqINT = 75;
    reqWIS = 40;
    importance = 10;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput()

  {
    selfOut = "#yYou snap your fingers and create a blinding flash of light!#N";
    obsvOut = "#y" + Mn + " snaps " + caster.his() + " fingers and creates a blinding flash of light!#N";

    grupOut = obsvOut;
    targOut = obsvOut;

    spellOutput(true, false);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public int getDamage()

  {
    int damage = (level / 2) + Utility.getRandomInt(100, 150);
    return damage;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}