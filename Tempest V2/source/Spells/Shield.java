class Shield extends SpellEffect

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Shield()

  {
    super();
    makeHelpful();

    id = SPELL_SHIELD;
    name = "Shield";
    desc = "A mystical shield protects from physical damage.";
    prof = "Kn Pa Ap Cl Dr Mo Pr Dc Sc";
    manacost = 20;
    reqSTR = 30;
    reqDEX = 20;
    reqINT = 45;
    reqWIS = 45;
    importance = 20;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void createEffect() { effect = new EffectShield(); }
  public void initEffect()   { effect.init(target, 1, 10);  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput(boolean self)

  {
    String t1 = tn;
    String t2 = tn;

    if (self) t1 = "yourself";
    if (self) t2 = target.himself();

    selfOut = "#CYou summon forth a mighty shield to protect " + t1 + ".#N";
    targOut = "#C" + Mn + " summons forth a mighty shield to protect you.#N";
    grupOut = "#C" + Mn + " summons forth a mighty shield to protect " + t2 + ".#N";
    obsvOut = "#C" + Mn + " summons forth a mighty shield to protect " + t2 + ".#N";

    spellOutput(true, false);
  }
  
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
  
  public void appendOutput(boolean self, boolean trainSpell)
  
  {
    if (self)  selfOut = "You are protected by Shield.#N";
    else       selfOut = Tn + " is protected by Shield.#N";

    targOut = "You are protected by Shield.#N";
    grupOut = Tn + " is protected by Shield.#N";
    obsvOut = Tn + " is protected by Shield.#N";

    spellOutput(false, trainSpell);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}