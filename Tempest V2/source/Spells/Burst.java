class Burst extends SpellAreaDamage

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Burst()

  {
    super();

    id = SPELL_BURST;
    name = "Burst";
    desc = "Unleash a barrage of energy bombs to devastate the area.";
    prof = "Wi Sc Wl";
    manacost = 100;
    importance = 150;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput()

  {
    selfOut = "#gYou wave your hand, and the very air begins to pop and explode!#N";
    obsvOut = "#g" + Mn + " waves " + caster.his() + " hand, causing the very air to pop and explode!#N";

    grupOut = obsvOut;
    targOut = obsvOut;

    spellOutput(true, false);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public int getDamage()

  {
    int damage = (level / 2) + Utility.getRandomInt(300, 500);
    return damage;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}