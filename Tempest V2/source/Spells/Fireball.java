class Fireball extends SpellDamage

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Fireball()

  {
    super();

    id = SPELL_FIREBALL;
    name = "Fireball";
    desc = "Hurl a deadly ball of fire to incinerate your enemy.";
    prof = "Mg Wi Sc Wl";
    manacost = 75;
    importance = 55;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput()

  {
    if (!target.isDead())

    {
      selfOut = "#rYou throw a fireball at " + tn + "!#N";
      targOut = "#r" + Mn + " throws a fireball you!#N";
      grupOut = "#r" + Mn + " throws a fireball at " + tn + "!#N";
      obsvOut = "#N" + Mn + " throws a fireball at " + tn + "!#N";
    }

    else

    {
      selfOut = "#rYour fireball completely incinerates " + tn + "!#N";
      targOut = "#r" + My + " fireball completely incinerates your body!#N";
      grupOut = "#r" + My + " fireball completely incinerates " + tn + "!#N";
      obsvOut = "#N" + My + " fireball completely incinerates " + tn + "!#N";
    }

    spellOutput(true, false);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public int getDamage()

  {
    int damage = (level / 5) + Utility.getRandomInt(150, 350);
    return damage;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}