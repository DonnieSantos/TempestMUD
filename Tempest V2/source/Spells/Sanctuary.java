class Sanctuary extends SpellEffect

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Sanctuary()

  {
    super();
    makeHelpful();

    id = SPELL_SANCTUARY;
    name = "Sanctuary";
    desc = "Divine protection against all damage.";
    prof = "Kn Pa Cl Mo Dr Pr Sc";
    manacost = 50;
    reqDEX = 40;
    reqINT = 55;
    reqWIS = 85;
    importance = 25;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void createEffect() { effect = new EffectSanctuary(); }
  public void initEffect()   { effect.init(target, 1, 5);      }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput(boolean self)

  {
    String t1 = tn;
    String t2 = tn;

    if (self) t1 = "you";
    if (self) t2 = target.him();

    selfOut = "#yYour prayers manifest into a shiny golden aura that surrounds " + t1 + ".#N";
    targOut = "#y" + My + " prayers manifest into a shiny golden aura that surrounds you.#N";
    grupOut = "#y" + My + " prayers manifest into a shiny golden aura that surrounds " + t2 + ".#N";
    obsvOut = "#y" + My + " prayers manifest into a shiny golden aura that surrounds " + t2 + ".#N";

    spellOutput(true, false);
  }
  
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
  
  public void appendOutput(boolean self, boolean trainSpell)
  
  {
    if (self)  selfOut = "You are protected by Sanctuary.#N";
    else       selfOut = Tn + " is protected by Sanctuary.#N";

    targOut = "You are protected by Sanctuary.#N";
    grupOut = Tn + " is protected by Sanctuary.#N";
    obsvOut = Tn + " is protected by Sanctuary.#N";

    spellOutput(false, trainSpell);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}