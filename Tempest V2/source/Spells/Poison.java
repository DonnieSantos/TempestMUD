class Poison extends SpellEffect

{
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private static final long serialVersionUID = 2000;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  private int poisonStrength;

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public Poison()

  {
    super();
    makeHarmful();

    id = SPELL_POISON;
    name = "Poison";
    desc = "Poison your enemy.";
    prof = "Mg Wi Sc Wl";
    manacost = 60;
    reqINT = 100;
    importance = 100;
    learnRate = 5;
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void createEffect() { effect = new EffectPoison(); }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void initEffect()

  {
    poisonStrength = (int)((((double)level / 100.0) * 15.0) + 5.0);
    effect.init(target, poisonStrength, 5);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void updateOutput(boolean self)

  {
    String t1 = tn;
    String t2 = tn;

    if (self) t1 = "you";
    if (self) t2 = target.him();

    selfOut = "#gYou conjure a dark cloud of green mist around " + t1 + ".#N";
    targOut = "#g" + My + " conjures a dark cloud of green mist around you.#N";
    grupOut = "#g" + My + " conjures a dark cloud of green mist around " + t2 + ".#N";
    obsvOut = "#g" + My + " conjures a dark cloud of green mist around " + t2 + ".#N";

    spellOutput(true, false);
  }
  
  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////

  public void appendOutput(boolean self, boolean trainSpell)

  {
    if (self)  selfOut = "You have been poisoned!#N";
    else       selfOut = Tn + " has been poisoned!#N";

    targOut = "You have been poisoned!#N";
    grupOut = Tn + " has been poisoned!#N";
    obsvOut = Tn + " has been poisoned!#N";

    spellOutput(false, trainSpell);
  }

  /////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////
}