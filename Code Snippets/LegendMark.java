class LegendMark extends MudObject

{
  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////

  private static final int NUM_VARS = 3;

  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////

  private static final int MARK  = 0;
  private static final int DATE  = 1;
  private static final int TIMES = 2;

  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////

  public MudString getMark()    { return ((MudString)vars[LegendMark.MARK]).get();   }
  public MudString getDate()    { return ((MudString)vars[LegendMark.DATE]).get();   }
  public MudInteger getTimes()  { return ((MudInteger)vars[LegendMark.TIMES]).get(); }

  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////

  public void setMark(MudString mark)     { vars[LegendMark.MARK] = mark;   }
  public void setDate(MudString date)     { vars[LegendMark.DATE] = date;   }
  public void setTimes(MudInteger times)  { vars[LegendMark.TIMES] = times; }

  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////

  public LegendMark(MudObject parent, MudString mark, MudString date, MudInteger times)

  {
    super(parent, NUM_VARS);
    set(LegendMark.MARK, mark);
    set(LegendMark.DATE, date);
    set(LegendMark.TIMES, times);
  }

  ///////////////////////////////////////////////////////////////////////////////////////
  ///////////////////////////////////////////////////////////////////////////////////////
}